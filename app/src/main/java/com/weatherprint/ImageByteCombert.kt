/**
 *  天気シンボルデータ生成
 *  晴：sunBits
 *  曇：cloudBits
 *  雨：rainBits
 *  雪：snowBits
 *  時々・一時：barBits
 *  のち：slashBits
 *  空白：spaceBits
 */
fun makeSymbol(telop: String) : MakeSymbolData {

    // シンボル印刷用data classの初期化（すべてspaceBitsで埋めておく）
    val makeSymbolData = MakeSymbolData(spaceBits, spaceBits, spaceBits)

    // 正規表現：時々・一時・のち 判定用
    val regexSometimes = Regex(".+時々.+")
    val regexTemporary = Regex(".+一時.+")
    val regexAfter = Regex(".+のち.+")

    // 正規表現：晴 の複合判定用
    val regexHeadSun = Regex("晴.+")
    val regexLastSun = Regex(".+晴")

    // 正規表現：曇 の複合判定用
    val regexHeadCloud = Regex("曇.+")
    val regexLastCloud = Regex(".+曇")

    // 正規表現：雨 の複合判定用
    val regexHeadRain = Regex("雨.+")
    val regexLastRain = Regex(".+雨")

    // 正規表現：雪 の複合判定用
    val regexHeadSnow = Regex("雪.+")
    val regexLastSnow = Regex(".+雪")

    // 正規表現：止む の複合判定用
    val regexLastStop = Regex(".+止む")

    // 以下、テロップ文字列から天気シンボルのデータを生成
    // 「晴れ」のみ
    if (telop=="晴れ") {
        makeSymbolData.secondLetter = sunBits
    }
    // 「曇り」のみ
    else if (telop=="曇り") {
        makeSymbolData.secondLetter = cloudBits
    }
    // 「雨」のみ
    else if (telop=="雨") {
        makeSymbolData.secondLetter = rainBits
    }
    // 「雪」のみ
    else if (telop=="雪") {
        makeSymbolData.secondLetter = snowBits
    }
    // 「時々」「一時」「のち」を含む
    else if (telop.matches(regexSometimes) ||
             telop.matches(regexTemporary) ||
             telop.matches(regexAfter)) {

        // セカンドトレターの判定
        if (telop.matches(regexAfter)) {
            // 「のち」→「／」
            makeSymbolData.secondLetter = slashBits
        } else if (telop.matches(regexSometimes) || telop.matches(regexTemporary)) {
            // 「時々」「一時」→「｜」
            makeSymbolData.secondLetter = barBits
        }

        // ファーストレターの判定
        if (telop.matches(regexHeadSun)) {
            // 晴
            makeSymbolData.firstLetter = sunBits
        }
        else if (telop.matches(regexHeadCloud)) {
            // 曇
            makeSymbolData.firstLetter = cloudBits
        }
        else if (telop.matches(regexHeadRain)) {
            // 雨
            makeSymbolData.firstLetter = rainBits
        }
        else if (telop.matches(regexHeadSnow)) {
            // 雪
            makeSymbolData.firstLetter = snowBits
        }

        // サードレターの判定
        if (telop.matches(regexLastSun)) {
            // 晴
            makeSymbolData.thirdLetter = sunBits
        }
        else if (telop.matches(regexLastCloud)) {
            // 曇
            makeSymbolData.thirdLetter = cloudBits
        }
        else if (telop.matches(regexLastRain)) {
            // 雨
            makeSymbolData.thirdLetter = rainBits
        }
        else if (telop.matches(regexLastSnow)) {
            // 雪
            makeSymbolData.thirdLetter = snowBits
        }
        else if (telop.matches(regexLastStop)) {
            // 止む（「雨時々止む」「雪時々止む」という形で使用されるので「曇り」を割り当てる）
            makeSymbolData.thirdLetter = cloudBits
        }
    }

    return makeSymbolData
}


/**
 *  天気シンボルデータから倍t列を生成
 */
fun symbolByteArray(firstLetter: String?, secondLetter: String?, thirdLetter: String?): ByteArray {

    val outByteArray = ByteArrayOutputStream()

    // シンボル：1st letter
    val firstByteArray = firstLetter
        ?.chunked(8)
        ?.map {
            it.toInt(2).toByte()
        }

    // シンボル：2st letter
    val secundByteArray = secondLetter
        ?.chunked(8)
        ?.map {
            it.toInt(2).toByte()
        }

    // シンボル：3st letter
    val thirdByteArray = thirdLetter
        ?.chunked(8)
        ?.map {
            it.toInt(2).toByte()
        }

    // シンボルを連結して一つの画像にする
    outByteArray.write(firstByteArray?.toByteArray())
    outByteArray.write(secundByteArray?.toByteArray())
    outByteArray.write(thirdByteArray?.toByteArray())

    // 縦長にするために同じイメージをもう一度出力
    outByteArray.write(firstByteArray?.toByteArray())
    outByteArray.write(secundByteArray?.toByteArray())
    outByteArray.write(thirdByteArray?.toByteArray())

    // バイトストリーム打ち止め
    outByteArray.close()

    // 戻り値として出力
    return outByteArray.toByteArray()
}

